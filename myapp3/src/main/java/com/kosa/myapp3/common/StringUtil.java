package com.kosa.myapp3.common;

public class StringUtil {
	// 생성자 private로 만들어서 객체 생성못하게
	private StringUtil() {	}
	
	public static String nullToValue(Object obj, String value) {
		// 전달된 객체가 null일 경우 두 번째 인자가 전달한 값을 반환
		if(obj==null)
			return value;
		else 
			// null이 아닐 경우 obj의 toString값 전달
			return obj.toString();
		
		// String 객체에만 사용가능
		// 객체 안만들고 함수 사용이 가능하게 하기위해 static 키워드 사용
	}
}
